package com.tnsif.intro;

public class loops {
	public static void main(String[] args)
	{
		for(int i=0;i<=10;i++)
		{
			System.out.println("HI");
			
		}
		int a=0;
		while(a<=10)
		{
			System.out.println("Hello");
			a++;
		}
		int b=0;
		do {
			System.out.println("*");
			b++;
		}while(b<=5);
	}
	
	}


