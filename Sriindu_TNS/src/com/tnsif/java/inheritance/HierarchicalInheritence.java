package com.tnsif.java.inheritance;

public class HierarchicalInheritence {
	public void display() {
		System.out.println("Parent Class Display Method");
	}
}
class Child1 extends HierarchicalInheritence{
	public void show() {
		System.out.println("Child1 class show method");
	}
}
class Child2 extends HierarchicalInheritence{
	public void show() {
		System.out.println("Child2 class show method");
	}
}
class Child3 extends HierarchicalInheritence{
	public void show() {
		System.out.println("Child3 class show method");
	}
}
	
